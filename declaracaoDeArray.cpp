#include <stdio.h>

int main() {
    // Declarando um array de inteiros com 5 elementos
    int array[5] = {10, 20, 30, 40, 50};

    //Acessando e imprimindo os elementos do array
    printf("Elemento 1: %d\n", array[0]); // Primeiro elemento (índice 0)
    printf("Elemento 2: %d\n", array[1]); // Segundo elemento (índice 1)
    printf("Elemento 3: %d\n", array[2]); // Terceiro elemento (índice 2)
    printf("Elemento 4: %d\n", array[3]); // Quarto elemento (índice 3)
    printf("Elemento 5: %d\n", array[4]); // Quinto elemento (índice 4)

    return 0;
}